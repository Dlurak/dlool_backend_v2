/** Seconds in a minute */
export const MINUTE = 60;

/** Seconds in half a hour */
export const HALF_HOUR = 30 * MINUTE;

/** Seconds in a hour */
export const HOUR = 60 * MINUTE;

/** Seconds in a day */
export const DAY = 24 * HOUR;

/** Seconds in a week */
export const WEEK = 7 * DAY;

/** Seconds in a month */
export const MONTH = 30 * DAY;

/** Seconds in a half year */
export const HALF_YEAR = 6 * MONTH;

/** Seconds in a year */
export const YEAR = 12 * MONTH;
